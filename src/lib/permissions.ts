  // Ventas
  CREAR_VENTAS: [ROLES.ADMIN, ROLES.SUPERVENDEDOR, ROLES.VENDEDOR],
  VER_VENTAS: [ROLES.ADMIN, ROLES.SUPERVENDEDOR, ROLES.VENDEDOR],
  VER_REPORTES: [ROLES.ADMIN, ROLES.SUPERVENDEDOR],

